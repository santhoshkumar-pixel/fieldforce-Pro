export function nextJobId(tickets) {
  const year = new Date().getFullYear();
  const nums = tickets
    .map((t) => t.id.match(/JOB-\d+-(\d+)/))
    .filter(Boolean)
    .map((m) => parseInt(m[1], 10));
  const next = (nums.length ? Math.max(...nums) : 0) + 1;
  return `JOB-${year}-${String(next).padStart(3, "0")}`;
}
